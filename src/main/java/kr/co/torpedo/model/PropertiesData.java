package kr.co.torpedo.model;

public class PropertiesData {
	private int fileNum, lottoset, folderFileNum, threadNum;
	private String dir;

	public int getFileNum() {
		return fileNum;
	}

	public void setFileNum(int fileNum) {
		this.fileNum = fileNum;
	}

	public int getLottoset() {
		return lottoset;
	}

	public void setLottoset(int lottoset) {
		this.lottoset = lottoset;
	}

	public int getFolderFileNum() {
		return folderFileNum;
	}

	public void setFolderFileNum(int folderFileNum) {
		this.folderFileNum = folderFileNum;
	}

	public int getThreadNum() {
		return threadNum;
	}

	public void setThreadNum(int threadNum) {
		this.threadNum = threadNum;
	}

	public String getDir() {
		return dir;
	}

	public void setDir(String dir) {
		this.dir = dir;
	}
}
